package iniciante;

public class Uri_2752 {
    public static void main(String[] args) {
        String a = "AMO FAZER EXERCICIO NO URI";
        System.out.printf("<%s>%n", a);
        System.out.printf("<%30s>%n", a);
        System.out.printf("<%.20s>%n", a);
        System.out.printf("<%-20s>%n", a);
        System.out.printf("<%-30s>%n", a);
        System.out.printf("<%.30s>%n", a);
        System.out.printf("<%30.20s>%n", a);
        System.out.printf("<%-30.20s>%n", a);
    }
}
